#include<bits/stdc++.h>
using namespace std;
#define endl "\n"

int32_t main(){
    int t;
    cin >> t;
    while(t--){
        int n;
        cin >> n;
        
        for(int i = 1; i <= n; i++){
            cout << i << " ";
        }
        cout << endl;
    }

    return 0;
}